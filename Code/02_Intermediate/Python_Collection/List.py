# ========================================================= LIST =========================================================

# List is a collection which is ordered and changeable. Allows duplicate members.

# Creating a List
my_list = ["Manish", "Yadhuvanshi", "Manish", 21, 854327]

# Access Items
print(my_list) # this will print whole list
print(len(my_list))
print(my_list[3]) # this will print 854327
print(my_list[0]) # this will print Manish

# Negative Indexing
print(my_list[-1]) # this will print 854327
print(my_list[-3]) # this will print Y5432adhuvanshi
