# ========================================================= LOOPS =========================================================

# ---------------------------------------------------------- FOR LOOP ----------------------------------------------------------

for i in range(0, 10):
    print(i,": Jay Shree Ram")


# example using list
fruits = ["apple", "orange", "banana"]
for fruit in fruits:
    print(fruit)

